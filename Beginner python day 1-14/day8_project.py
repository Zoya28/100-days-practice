def cypher(direction,text,shift):
    cypher_text=""
    if direction=="decode":
        shift *= -1
    for letter in text:
        if letter in alphabet:
            position=alphabet.index(letter)
            new_pos= position+shift
            cypher_text += alphabet[new_pos]
        else:
            cypher_text += letter    
    print(f"The {direction}d text is :-\n{cypher_text} \n")

